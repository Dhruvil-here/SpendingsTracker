    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)jTable2.getModel();
            int rc = dtm.getRowCount();

            while(rc -- != 0){
                dtm.removeRow(0);
            }

            java.sql.Date dt1 = new java.sql.Date(d1.getDate().getTime());
            java.sql.Date dt2 = new java.sql.Date(d2.getDate().getTime());
            Connection c=DriverManager.getConnection("jdbc:mysql://localhost:3306/spendingdb"+"?openSSL=false","root", "Vasu@2004");
            Statement s=c.createStatement ();
            java.sql.ResultSet rs = s.executeQuery("select * from spendings where sdate>='"+dt1+"' and sdate<='"+dt2+"' order by sdate asc ");

            int total = 0;
            while(rs.next()){
                total += rs.getInt("amount");
                Object o[] = { rs.getDate("sdate"), rs.getString("category"), rs.getInt("amount")};
                dtm.addRow(o);
                jTextField2.setText(total+"");
            }

        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

