    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            java.util.Date dt = jDateChooser1.getDate();
            String s1 = jTextField1.getText();
            String s2 = (String)jComboBox1.getSelectedItem();
            if (dt != null && !s1.equals("") && !s2.equals("")){
                int amount = Integer.parseInt(s1);
                java.sql.Date date = new java.sql.Date(dt.getTime());
                Connection c=DriverManager.getConnection("jdbc:mysql://localhost:3306/spendingdb"+"?openSSL=false","root", "Vasu@2004");
                Statement s=c.createStatement ();
                s.executeUpdate("insert into spendings (category, sdate, amount) values('"+s2+"','"+date+"','"+amount+"') ");
                JOptionPane.showMessageDialog(null, "Expense added successfully!!");
                getEntries();
            } else {
                JOptionPane.showMessageDialog(null, "Please fill all deatils!!");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

