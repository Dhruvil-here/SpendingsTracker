    private void displayCategory(){
        try{
            jComboBox1.removeAllItems();
            Connection c=DriverManager.getConnection("jdbc:mysql://localhost:3306/spendingdb"+"?openSSL=false","root", "Vasu@2004");
            Statement s=c.createStatement ();
            java.sql.ResultSet rs = s.executeQuery("select * from category_info");
            while(rs.next()){
                jComboBox1.addItem(rs.getString("category"));
            }
        }catch(Exception ex){
           JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    private void getEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)jTable1.getModel();
            
            int rc = dtm.getRowCount();
            
            while(rc -- != 0){
                dtm.removeRow(0);
            }
            
            java.time.LocalDate cd = java.time.LocalDate.now(); //cd = current date
            java.time.LocalDate bd = cd.minusDays(30); //bd = before date
            Connection c=DriverManager.getConnection("jdbc:mysql://localhost:3306/spendingdb"+"?openSSL=false","root", "Vasu@2004");
            Statement s=c.createStatement ();
            java.sql.ResultSet rs = s.executeQuery("select * from spendings where sdate<='"+cd+"' and sdate>='"+bd+"' ");
            int total = 0;
            
            while(rs.next()){
                total += rs.getInt("amount");
                Object o[] = {rs.getInt("sid"), rs.getString("category"), rs.getDate("sdate"), rs.getInt("amount")};
                dtm.addRow(o);
            }
            jLabel7.setText(total+"");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }

